# SECUENCIAS DE ESCAPE
#Las secuencias de escape en Python se usan para representar caracteres especiales dentro de strings, como saltos de línea, tabulaciones, comillas dentro de comillas, etc.

# Salto de línea \n
print("Hola\nMundo")                #Hola
                                    #Mundo

# Tabulación \t
print("Hola\tMundo")                #Hola   Mundo

# Barra invertida literal \
print("Esto es una barra invertida: \\")        #Esto es una barra invertida: \

# Comilla simple dentro de comillas simples (usando escape)   \  
print('Es una contraccion: It\'s fine')         #Es una contraccion: It's fine

# Comilla doble dentro de comillas dobles (usando escape) \...\
print("Ella dijo: \"Hola\"")                    #Ella dijo: "Hola"


